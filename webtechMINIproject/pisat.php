            <h1>Evolution Of PIsat</h1>
    <p>
        PES University (Formerly known as PES Institute Of Technology) students initiated the small satellite development program in 2011. During the initial days of development, the brainstorming of satellite configuration was carried out by the students of PES. Initially for students’ to experience satellite development we opted to use Inter Orbital System’s developed TUBESAT as a platform. But due to its very limited mission life of 1 month this option was not considered. Further, we studied CubeSat based architecture of 1U (10 x 10 x 10 cm) configuration. 1U structure can be stacked vertically or horizontally to form 2U/3U satellite. The 3U satellite configuration was not opted due to insufficient power generation capability and un-optimized weight considerations for imaging payload and S Band communication system.</p>
            
                </article>
            </div>
    
    
    <div class='second-container'>
            <article>
            <h1>Structure Of PIsat</h1>
    <p>
        Finally a custom made cuboidal structure was evolved to support sufficient power generation and weight optimization. With this structure a simple spin stabilized imaging satellite was proposed to ISRO. Later, ISRO suggested for three axis stabilized imaging satellite which is the current PISAT configuration.

PISAT is configured with various subsystems namely: Payload, an imaging camera, structure, thermal, communication system, control, actuators and sensors, on-board computers, electrical systems and mission aspects.</p>
            
